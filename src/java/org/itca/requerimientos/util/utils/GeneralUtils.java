package org.itca.requerimientos.util.utils;

/**
 *
 * @author dfhernandez
 */
public class GeneralUtils {

    public GeneralUtils() {
    }
    
    public String removeAccentMarks(String cadena) {
        return cadena.replace("Á", "A")
                .replace("É", "E")
                .replace("Í", "I")
                .replace("Ó", "O")
                .replace("Ú", "U")
                .replace("Ñ", "N")
                .replace("á", "a")
                .replace("é", "e")
                .replace("í", "i")
                .replace("ó", "o")
                .replace("ú", "u")
                .replace("ñ", "n");
    }
    
}
